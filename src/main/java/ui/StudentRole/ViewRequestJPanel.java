    private void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);

        ArrayList<WorkRequest> workRequests = account.getWorkQueue().getWorkRequestList();

        for (WorkRequest request : workRequests) {
            if (request.getRequester().equals(account)) {
                Object[] row = new Object[6];
                row[0] = request.getId();
                row[1] = request.getType();
                row[2] = request.getDescription();
                row[3] = request.getLocation();
                row[4] = request.getRequestDate();
                row[5] = request.getStatus();
                model.addRow(row);
            }
        }
    }

