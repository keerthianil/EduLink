    private void DeleteRequesBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteRequesBtnActionPerformed
        int selectedRow = jTable2.getSelectedRow();
    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(this, "Please select a request to delete.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Retrieve the WorkRequest object from the selected row
    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    int requestId = (int) model.getValueAt(selectedRow, 0); // Assuming Request ID is in column 0

    // Find and remove the WorkRequest from the user's WorkQueue
    WorkRequest toDelete = null;
    for (WorkRequest request : account.getWorkQueue().getWorkRequestList()) {
        if (request.getId() == requestId) {
            toDelete = request;
            break;
        }
    }

    if (toDelete != null) {
        account.getWorkQueue().getWorkRequestList().remove(toDelete);
        JOptionPane.showMessageDialog(this, "Request deleted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this, "Request not found in the WorkQueue.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    // Refresh the table
    populateRequestTable();
        
        
        

    }//GEN-LAST:event_DeleteRequesBtnActionPerformed

