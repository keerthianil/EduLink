    public void populateFundRequestsTable() {
     DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    model.setRowCount(0);

    List<WorkRequest> workRequests = system.getWorkQueue().getWorkRequestList();
    for (WorkRequest request : workRequests) {
        if (request instanceof FundRequest) {
            FundRequest fundRequest = (FundRequest) request;
            Object[] row = new Object[6];
            row[0] = fundRequest.getId();
            row[1] = fundRequest.getType();
            row[2] = fundRequest.getRequester().getEmployee().getName();
            row[3] = fundRequest.getPrice();
            row[4] = fundRequest.getAmountFunded();
            row[5] = fundRequest.getStatus();
            model.addRow(row);
        }
    }
}



