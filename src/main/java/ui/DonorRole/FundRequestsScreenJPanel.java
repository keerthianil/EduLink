    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // the row selected on the table's information is displayed in the text fields of the Jpanel called FundRequestsDialogJPanel 
        // if no row is selected, a message is displayed to the user to select a row
        // if a row is selected, the user is taken to the FundRequestsDialogJPanel to view the details of the request

        int selectedRow = jTable2.getSelectedRow();
    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(this, "Please select a row to view details!", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Get the selected FundRequest
    int requestId = (int) jTable2.getValueAt(selectedRow, 0); // ID column
    FundRequest fundRequest = (FundRequest) system.getWorkQueue().getWorkRequestById(requestId);

    if (fundRequest == null) {
        JOptionPane.showMessageDialog(this, "Unable to find the selected request. Please refresh and try again.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Navigate to FundRequestsDialogJPanel
    FundRequestsDialogJPanel panel = new FundRequestsDialogJPanel(userProcessContainer, system, donorAccount, fundRequest);
    userProcessContainer.add("FundRequestsDialogJPanel", panel);
    CardLayout layout = (CardLayout) userProcessContainer.getLayout();
    layout.next(userProcessContainer);














    }//GEN-LAST:event_jButton2ActionPerformed

