    private void populateStudentComboBox() {
    studentNameCombobox.removeAllItems(); // Clear existing items

    // Fetch students assigned to this advisor
    for (UserAccount student : system.getUserAccountDirectory().getUserAccountList()) {
        if (student.getAssignedAdvisor() != null && student.getAssignedAdvisor().equals(advisor)) {
            studentNameCombobox.addItem(student); // Add student UserAccount
        }
    }
}



















