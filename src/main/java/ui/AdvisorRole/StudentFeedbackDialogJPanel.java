    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       UserAccount selectedStudent = (UserAccount) studentNameCombobox.getSelectedItem();
        if (selectedStudent == null) {
            JOptionPane.showMessageDialog(this, "Please select a student.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String recommendationText = jTextField2.getText().trim();
        if (recommendationText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student Strengths cannot be empty.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Create a new RecommendationRequest
        RecommendationRequest recommendationRequest = new RecommendationRequest(
            selectedStudent.getEmployee().getName(),
            recommendationText,
            advisor,
            selectedStudent
        );

        // Add the request to the system's work queue
        system.getWorkQueue().addWorkRequest(recommendationRequest);

        JOptionPane.showMessageDialog(this, "Recommendation saved successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);

        // Clear the fields
        jTextField2.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

