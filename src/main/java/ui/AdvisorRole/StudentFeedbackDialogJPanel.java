    private void populateStudentStrength(java.awt.event.ActionEvent evt) {
        UserAccount selectedStudent = (UserAccount) studentNameCombobox.getSelectedItem();
        if (selectedStudent != null) {
            // Fetch the latest "description" from the student's work requests
            for (WorkRequest request : selectedStudent.getWorkQueue().getWorkRequestList()) {
                if (request.getType().equals("Class Request")) {
                    jTextField2.setText(request.getDescription());
                    break;
                }
            }
        }
    }

